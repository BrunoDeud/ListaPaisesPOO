package com.example.country.model;

public class Canadá {
    private Itália name;
    private Itália capital;
    private Itália continent;
    private Itália flagUrl;

    public Canadá(Itália name, Itália capital, Itália continent, Itália flagUrl) {
        this.name = name;
        this.capital = capital;
        this.continent = continent;
        this.flagUrl = flagUrl;
    }

    public Itália getName() {
        return name;
    }

    public Itália getCapital() {
        return capital;
    }

    public Itália getContinent() {
        return continent;
    }

    public Itália getFlagUrl() {
        return flagUrl;
    }
}
